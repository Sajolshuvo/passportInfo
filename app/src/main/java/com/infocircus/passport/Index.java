package com.infocircus.passport;

public class Index {

    private String mName;
    private int imageSrc;

    public Index(String name, int imageSrc) {
        this.mName = name;
        this.imageSrc = imageSrc;
    }

    public String getmName() {
        return mName;
    }

    public int getImageSrc() {
        return imageSrc;
    }
}
